    //==================================================================================================================
    // Constructor(s)
    //==================================================================================================================

    public HTextArea(String text) {

        this.text = text;
        generateData();

    }


    //==================================================================================================================
    // Getter(s)& Setter(s)
    //==================================================================================================================

    //==================================================================================================================
    // Public Functions(s)
    //==================================================================================================================

    public void generateData()
    {
        textArea = new JTextArea(text);
    }

