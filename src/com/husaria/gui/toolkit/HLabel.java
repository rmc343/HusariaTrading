package com.husaria.gui.toolkit;

import javax.swing.*;

/**
 * Created by Owner on 8/18/2018.
 */
public class HLabel extends HComponent{

    //==================================================================================================================
    // Instance Variable(s)
    //==================================================================================================================
    private String text;

    private JLabel label;

    //==================================================================================================================
    // Constructor(s)
    //==================================================================================================================

    public HLabel() {
    }

    public HLabel(String text) {

        this.text = text;
        generateData();

    }


    //==================================================================================================================
    // Getter(s)& Setter(s)
    //==================================================================================================================

    //==================================================================================================================
    // Public Functions(s)
    //==================================================================================================================

    @Override
    public void generateData() {

        label = new JLabel(text);

    }

    //==================================================================================================================
    // Private Functions(s)
    //==================================================================================================================





}//****************************************************END OF FILE*****************************************************/


