package com.husaria.gui.toolkit;

import javax.swing.*;

/**
 * Created by Owner on 8/18/2018.
 */
public class HLabel extends HComponent{

    //==================================================================================================================
    // Instance Variable(s)
    //==================================================================================================================
    private String text;

    //==================================================================================================================
    // Constructor(s)
    //==================================================================================================================

    public HLabel(String text) {

        super(new JLabel());
        this.text = text;
        generateData();

    }


    //==================================================================================================================
    // Getter(s)& Setter(s)
    //==================================================================================================================

    public JLabel getComponent()
    {

        return  (JLabel)super.getComponent();

    }

    public void setComponent(JLabel label)
    {

        super.setComponent(label);

    }


    public String getText() {
        return getComponent().getText();
    }

    public void setText(String text) {
        getComponent().setText(text);
    }

    //==================================================================================================================
    // Public Functions(s)
    //==================================================================================================================



    @Override
    public void generateData() {

        setComponent(new JLabel(text));

    }

    @Override
    public void updateData() {

    }

    //==================================================================================================================
    // Private Functions(s)
    //==================================================================================================================





}//****************************************************END OF FILE*****************************************************/


